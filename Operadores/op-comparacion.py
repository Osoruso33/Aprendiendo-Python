# Siempre devuelven datos booleanos

# Igual que (==) 
esIgualQue = 5 == 6 # False

# Distinto de (!=) 
esDistintoDe = 5 != 6 # True

# Mayor y menor que (> y <)
esMayorQue = 5 > 6 # False

# Mayor/Menor o igual que (>= y <=)
esMenorOIgualQue = 5 <= 6 # True

